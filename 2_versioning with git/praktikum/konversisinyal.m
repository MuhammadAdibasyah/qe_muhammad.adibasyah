% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
t= 0:0.00001:2;
A=handles.A;
F=handles.F;
Fs=handles.Fs;
Fc=handles.Fc;
Ac=handles.Ac;
M=handles.M;
B=handles.M;
n=0:1/Fs:2;
s=A*sin(2*pi*F*t);
sn=A*sin(2*pi*F*n);
st=get(handles.popupmenu2,'value');
switch st
    case 1
        Xc=Ac*(1 + M*sin(2*pi*F*t)).*sin(2*pi*Fc*t);
    case 2
        Xc=Ac*cos(2*pi*Fc*t+B*sin(2*pi*F*t));
end
axes(handles.axes1)
plot(t,s),hold on,grid on,stem(n,sn);
axes(handles.axes2)
plot(t,Xc),grid on;



